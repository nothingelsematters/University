          translate :: [Expression]
          translate | isHypothesis || isCommonAxiom = addDoubleNegation
                    | isClassicAxiom                = translateClassicalAxiom
                    | otherwise                     = translateModusPonens
          
          update :: LineHandler
          update = lh { expressions = expr `Set.insert` (expressions lh),
                        parts = updateParts expr }
          updateParts (BinaryOperator Implication l r) =
              if Map.member r $ parts lh
                  then Map.update (\x -> Just (l:x)) r $ parts lh
                  else Map.insert r [l] $ parts lh
          updateParts _ = parts lh
          
          -- check zone --
          isHypothesis   = Set.member expr (lhhypotheses lh)
          isClassicAxiom = expr @? classicAxiom
          isCommonAxiom  = helper commonAxioms
              where helper []    = False
                    helper (h:t) = if expr @? h then True else helper t
                    
          infix 4 @? -- does expression fit axiom
          (@?) :: Expression -> Expression -> Bool
          expr @? pattern = compHelper expr pattern /= Nothing
              where compHelper (BinaryOperator op1 exl exr) (BinaryOperator op2 patl patr) =
                        if op1 /= op2
                            then Nothing
                            else compAssoc (compHelper exl patl) (compHelper exr patr)
                    compHelper (Not expr) (Not pattern) = compHelper expr pattern
                    compHelper expr (Variable s) = Just (Map.singleton s expr)
                    compHelper _ _ = Nothing

                    compAssoc _ Nothing = Nothing
                    compAssoc Nothing _ = Nothing
                    compAssoc left (Just right) = Map.foldlWithKey' assocFolder left right

                    assocFolder Nothing _ _ = Nothing
                    assocFolder (Just was) f s
                        | Map.lookup f was == Nothing = Just (Map.insert f s was)
                        | Map.lookup f was == Just s  = Just was
                        | otherwise                   = Nothing
                        
          getLeftMP :: Expression
          getLeftMP = fromJust $ foldr folder Nothing $ (parts lh) Map.! expr
              where folder _ res@(Just smth) = res
                    folder expr _ =
                        if expr `Set.member` (expressions lh)
                            then Just expr
                            else Nothing

          -- process zone --
          addDoubleNegation       = addDoubleNegationTemplate ~> (Map.fromList [("A", expr)])
          translateModusPonens    = modusPonensTemplate       ~> (Map.fromList [("A", getLeftMP), ("B", expr)])
          translateClassicalAxiom = helper expr
              where helper (BinaryOperator Implication l r) = classicAxiomTemplate ~> (Map.fromList [("A", r)])
                    helper _ = undefined
                    
          infix 4 ~> -- maps template
