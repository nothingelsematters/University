commonAxioms :: [Expression]
commonAxioms = map getExpression
    [
        "A -> (B -> A)",
        "(A -> B) -> (A -> B -> G) -> (A -> G)",
        "A -> B -> A & B",
        "A & B -> A",
        "A & B -> B",
        "A -> A | B",
        "B -> A | B",
        "(A -> G) -> (B -> G) -> (A | B -> G)",
        "(A -> B) -> (A -> !B) -> !A"
    ]

classicAxiom :: Expression
classicAxiom = getExpression "!!A -> A"

data LineHandler = LineHandler { lhhypotheses :: Set.HashSet Expression,
                                 expressions  :: Set.HashSet Expression,
                                 parts        :: Map.HashMap Expression [Expression] }
                                 
newLineHandler :: [Expression] -> LineHandler
newLineHandler hyps = LineHandler (Set.fromList hyps) (Set.empty) (Map.empty)

doubleNegate :: LineHandler -> Expression -> (LineHandler, [Expression])
doubleNegate lh expr = (update, translate)
